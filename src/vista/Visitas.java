    private void cargarModelo()
    {
        tiposv=ctipov.findUsuarioEntities();
        for(Usuario tv: tiposv)
        {
            stipo.addItem(tv.getNombre());
        }
    }
    private void agregarVIsita()
    {
        vehiculo=new Visita();
        String hora=horaIngreso.getText();
        Time nuevot=Time.valueOf(hora);
        vehiculo.setHoraIngreso(nuevot);
       
        
        vehiculo.setIdUsuario(tiposv.get(stipo.getSelectedIndex()));
        cvehiculo.create(vehiculo);
    }

